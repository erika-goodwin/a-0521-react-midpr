package com.company;

public class AssignmentOpearator {
    public static void main(String[] args){
        int x = 5;
        x += 5; // x = X + 5 //10
        x -= 5; // x = x - 5 //5
        x *= 5; // x = x * 5 //25
        x /= 5; // x = x / 5 //5
        x %= 5; // x = x % 5 //0
        System.out.println(x);
    }
}
